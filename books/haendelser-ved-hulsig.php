	<p>
”Hændelser ved Hulsig” er både en egnsskildring af et lille forblæst sogn syd for Skagen og en 

række fortællinger om livet og menneskene på en egn, der igennem århundreder var truet af 

naturkatastrofer, i form af oversvømmelser og sandflugt. Beboerne overlevede den barske natur 

bl.a. ved aktivt at spille med i naturens barske drama: en stor del af deres indtægter fik de fra de 

mange skibsforlis, og det udsatte liv, de måtte føre, skabte en stor solidaritet imellem dem. Når 

gården sandede til, hjalp man hinanden med at flytte ruinerne fra det gamle væk og bygge nyt op et 

mindre udsat sted. Og når mennesker derude i stormen var i livsfare, stod man sammen i forsøget på 

at redde dem fra kulde-og druknedød. 
</p>
<p>
Befolkningen levede i mange generationen i stor isolation, fanget som den var af store, ustabile 

sandmiler mod syd/vest (Ålbæk) og mod nord/øst (Skagen) – den eneste vej væk fra Hulsig gik 

langs strandene på begge sider af den smalle Skagen Odde. Og de var ufarbare i dårligt vejr, og 

klimaet var hårdere her end i resten af landet.
</p>
<p>

 Den store isolation (og traditionen) bevirkede, at man helst valgte ægtemage fra de nærmeste 

gårde, og denne ”indavl” skabte sammen med de øvrige livsvilkår en befolkning, der på én gang var 

hårdfør (udadtil) og samtidig skrøbelig ( indadtil i sindet). Lars Hesselholt skriver i sine erindringer: 

”Hulsig-minder”, at folk i Hulsig og omegn var ”som en stor familie”. Ja, men det var de skam også 

i bogstaveligste forstand.
</p>
<p>

Bogen strækker sig over et tidsrum fra ca. 1805 til ca.1940. Her i sidste halvdel af perioden viste 

den lille befolknings styrke sig ved ikke blot at overleve mellemkrigstidens kriser, men ved at 

overvinde dem og gå i spidsen for den proces, der for altid skulle ændre det danske landbrug. Det 

var f.eks. et af de allerførste steder i Danmark, hvor traktoren afløste hesten.  Det skete i 1926.

